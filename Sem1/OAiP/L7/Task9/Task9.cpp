int checkDay2(int x) {
    int k;
    for (auto i = 0; i < numbers.size(); i++) //O(n)
        if (numbers[i] == x) {
            k = i;
            break;
        }
    int t = 1;
    std::cout << "Умерли во второй день:";
    while (k) { // O(log(n))
        if (k & 1) {
            std::cout << t << " ";
        }
        t++;
        k >>= 1;
    }
    std::cout << "\nНомер бочки\n";
    std::cout << x;
}
